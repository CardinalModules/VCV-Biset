QuantNote::QuantNote(float cent) {
	this->type = QUANT_CENT;
	this->numerator = cent;
	this->denominator = 1.0;
}

QuantNote::QuantNote(int numerator, int denominator) {
	this->type = QUANT_RATIO;
	this->numerator = numerator;
	this->denominator = denominator;
}
