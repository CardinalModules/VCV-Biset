RegexSeq::RegexSeq(void) {
	this->seq = NULL;
	this->seq_next = NULL;
}

RegexSeq::~RegexSeq(void) {
	if (this->seq != NULL)
		delete this->seq;
	if (this->seq_next != NULL)
		delete this->seq_next;
}
